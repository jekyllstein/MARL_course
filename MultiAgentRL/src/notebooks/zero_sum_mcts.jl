# ╔═╡ 842bd8ee-3be4-4ba0-b0a6-27c5e11c92db
function create_othello_game()
	#when it is not a player's turn, the only valid move is to pass
	is_valid_noturn(s::OthelloBoard, a::OthelloMove) = false
	is_valid_noturn(s::OthelloBoard, a::PassMove) = true

	function is_valid_action(s::OthelloBoard, p::OthelloPlayer, a::AbstractOthelloMove)
		s.current == p && return is_valid_move(s, a)
		is_valid_noturn(s, a)
	end

	is_valid_action(s::OthelloBoard, p::OthelloPlayer, i_a::Integer) = is_valid_action(s, p, OthelloMove(i_a))

	function step(s::OthelloBoard, a::Tuple{Int64, Int64})
		s′ = make_move(s, a[Int(s.current)])
		term = game_over(s′)
		black_win = winner(s′) == Black
		win_val = black_win*2f0 - 1f0
		reward = term*win_val
		return (reward, s′)
	end

	joint_action = (20, 65)
	ptf = StateZeroSumGameTransitionDeterministic(step, initialize_othello_board(); test_joint_action = joint_action)
	
	StateStochasticGame((Othello.INDEXED_MOVES, Othello.INDEXED_MOVES), ptf, initialize_othello_board, game_over; is_valid_action = ((s, i_a) -> is_valid_action(s, Black, i_a), (s, i_a) -> is_valid_action(s, White, i_a)))
end

# ╔═╡ 9b9144a1-42e4-4b8d-bc1f-a07b7c0cb06c
const othello_game = create_othello_game()

# ╔═╡ 3f0a4786-2a02-4b51-bcd4-b42b50318816
function othello_random(s::OthelloBoard, p::OthelloPlayer)
	if s.current == p
		moves = available_moves(s)
		m = rand(moves)
		othello_game.agent_action_index[1][m]
	else
		65
	end
end

# ╔═╡ 0c7292e7-b943-412a-83e0-6f3c4c945d5b
#play a random othello game where each player selects randomly from their valid moves
runepisode(othello_game; πs = ntuple(i -> s -> othello_random(s, OthelloPlayer(i)), 2))

